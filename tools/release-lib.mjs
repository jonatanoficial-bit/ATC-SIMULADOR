export function sha256(file) {
  return crypto.createHash('sha256').update(fs.readFileSync(file)).digest('hex');
}

export function listFiles(root, exclusions = []) {
  const result = [];
  const walk = dir => {
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const abs = path.join(dir, entry.name);
      const rel = path.relative(root, abs).replaceAll(path.sep, '/');
      if (exclusions.some(rule => rel === rule || rel.startsWith(`${rule}/`))) continue;
      if (entry.isDirectory()) walk(abs);
      else if (entry.isFile()) result.push(rel);
    }
  };
  walk(root);
  return result.sort();
}

export function writeManifest(root) {
  const exclusions = ['MANIFEST_SHA256.txt', 'dist', 'node_modules', '.git'];
  const files = listFiles(root, exclusions);
  const lines = files.map(rel => `${sha256(path.join(root, rel))}  ${rel}`);
  fs.writeFileSync(path.join(root, 'MANIFEST_SHA256.txt'), lines.join('\n') + '\n');
  return lines.length;
}
