import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';

export const ROOT = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
export const RELEASE_CONFIG = path.join(ROOT, 'config', 'release.json');

export function readJson(file) {
  return JSON.parse(fs.readFileSync(file, 'utf8'));
}

export function writeJson(file, value) {
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, JSON.stringify(value, null, 2) + '\n');
}

export function saoPauloStamp(date = new Date()) {
  const parts = Object.fromEntries(new Intl.DateTimeFormat('en-CA', {
    timeZone: 'America/Sao_Paulo', year: 'numeric', month: '2-digit', day: '2-digit',
    hour: '2-digit', minute: '2-digit', second: '2-digit', hourCycle: 'h23'
  }).formatToParts(date).filter(p => p.type !== 'literal').map(p => [p.type, p.value]));
  const compact = `${parts.year}${parts.month}${parts.day}-${parts.hour}${parts.minute}`;
  const display = `${parts.year}-${parts.month}-${parts.day} ${parts.hour}:${parts.minute} BRT`;
  const iso = `${parts.year}-${parts.month}-${parts.day}T${parts.hour}:${parts.minute}:${parts.second}-03:00`;
  return { compact, display, iso };
}

export function validateConfig(config) {
  const required = ['product','prefix','version','phase','phaseName','channel','saveSchema','contractSchema','testSchema','saveVaultSchema','pwaSchema','cacheSchema','uxSchema','desktopSchema','accessibilitySchema','replaySchema','target','timeZone'];
  for (const field of required) {
    if (config[field] === undefined || config[field] === null || config[field] === '') {
      throw new Error(`Campo obrigatório ausente em config/release.json: ${field}`);
    }
  }
  if (!/^\d+\.\d+\.\d+$/.test(config.version)) throw new Error('Versão deve seguir SemVer x.y.z');
  if (!/^F\d{2}$/.test(config.phase)) throw new Error('Fase deve seguir o padrão Fnn');
  if (!Number.isInteger(config.saveSchema) || config.saveSchema < 1) throw new Error('saveSchema inválido');
  if (!Number.isInteger(config.contractSchema) || config.contractSchema < 1) throw new Error('contractSchema inválido');
  if (!Number.isInteger(config.testSchema) || config.testSchema < 1) throw new Error('testSchema inválido');
  if (!Number.isInteger(config.saveVaultSchema) || config.saveVaultSchema < 1) throw new Error('saveVaultSchema inválido');
  if (!Number.isInteger(config.pwaSchema) || config.pwaSchema < 1) throw new Error('pwaSchema inválido');
  if (!Number.isInteger(config.cacheSchema) || config.cacheSchema < 1) throw new Error('cacheSchema inválido');
  if (!Number.isInteger(config.uxSchema) || config.uxSchema < 1) throw new Error('uxSchema inválido');
  if (!Number.isInteger(config.desktopSchema) || config.desktopSchema < 1) throw new Error('desktopSchema inválido');
  if (!Number.isInteger(config.accessibilitySchema) || config.accessibilitySchema < 1) throw new Error('accessibilitySchema inválido');
  if (!Number.isInteger(config.replaySchema) || config.replaySchema < 1) throw new Error('replaySchema inválido');
  if (config.aircraftPerformanceSchema !== undefined && (!Number.isInteger(config.aircraftPerformanceSchema) || config.aircraftPerformanceSchema < 1)) throw new Error('aircraftPerformanceSchema inválido');
  return true;
}

export function buildMetadata(config, date = new Date()) {
  validateConfig(config);
  const stamp = saoPauloStamp(date);
  const build = `${config.prefix}-${config.version}-${config.phase}-${stamp.compact}`;
  return { ...config, build, builtAt: stamp.display, builtAtIso: stamp.iso };
}

